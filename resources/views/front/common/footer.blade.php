    <script>
        $(".skiVideoControls .playBtn").click(function () {
            var clickedVideo = $(this).closest('.snippetsVideoChild').find('.skiVideo')[0];
            var allVideos = $('.skiVideo');
    
            // Pause all other videos
            allVideos.each(function () {
                if (this !== clickedVideo && !this.paused) {
                    this.pause();
                }
            });
    
            // Play the clicked video
            clickedVideo.play();
        });
    
        $(".skiVideoControls .pauseBtn").click(function () {
            $(this).closest('.snippetsVideoChild').find('.skiVideo')[0].pause();
        });
    
        $(".skiVideo").on("play", function () {
            $(this).closest('.snippetsVideoChild').find('.skiVideoControls').addClass("playing");
        });
    
        $(".skiVideo").on("pause", function () {
            $(this).closest('.snippetsVideoChild').find('.skiVideoControls').removeClass("playing");
        });
    
        $(".skiVideo").on("timeupdate", function () {
            var video = this;
            var progress = (video.currentTime / video.duration) * 100;
            $(this).closest('.snippetsVideoChild').find('.vidoeProgressBar').css('width', progress + '%');
        });
